export function formatDate(d) {
  return `${d.getFullYear()}-${d.getMonth() + 1}-${d.getDate()}`
}

export function readableDate(d) {
  const day = d.getDate()
  const month = d.getMonth() + 1
  const year = d.getFullYear()

  return `${day}.${month}.${year}`
}

